const out = {};
for (const loc of LOCALE_ORDER) {
  out[loc] = {};
  for (const key of Object.keys(en)) {
    if (GLOSSARY.has(key)) {
      out[loc][key] = en[key];
    } else if (T[loc]?.[key]) {
      out[loc][key] = T[loc][key];
    } else {
      throw new Error(`Missing translation: ${loc}.${key}`);
    }
  }
}

for (const loc of LOCALE_ORDER) {
  if (PATCHES[loc]) Object.assign(out[loc], PATCHES[loc]);
}

const outPath = path.resolve(__dirname, 'translation-packs/users-detail-full.json');
fs.writeFileSync(outPath, JSON.stringify(out, null, 2) + '\n', 'utf8');
console.log('Wrote', outPath);

let failed = false;
for (const loc of LOCALE_ORDER) {
  const same = [];
  for (const k of Object.keys(en)) {
    if (!out[loc][k]) same.push('MISSING:' + k);
    else if (!GLOSSARY.has(k) && out[loc][k] === en[k]) same.push(k);
  }
  const msg = same.length ? 'FAIL ' + same.length + ' ' + same.slice(0, 5).join(',') : 'OK';
  console.log(loc, msg);
  if (same.length) failed = true;
}
process.exit(failed ? 1 : 0);
